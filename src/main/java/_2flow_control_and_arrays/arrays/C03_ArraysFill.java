package _2flow_control_and_arrays.arrays;

public class C03_ArraysFill {
    // One-dimensional array

    public static void main(String[] args) {
        // On line 10, create an array of integer elements named array
        // and fill it with the values 1,2,3,4,5

        int[] array = new int[]{30, 2, 3, 4, 5};

        // print values of array elements.
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}
