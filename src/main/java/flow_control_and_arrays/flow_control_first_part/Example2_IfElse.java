package flow_control_and_arrays.flow_control_first_part;

public class Example2_IfElse {

    public static void main(String[] args) {

        int a = 10, b = 20;

        if (a > b) {
            System.out.println(a + " > " + b);
        } else {
            System.out.println(a + " < " + b);
        }
    }
}
